# 방법 1
print("나는 %d살입니다." % 20) # %의 자리에 정수 digit를 넣겠다
print("나는 %s을 좋아해요." % "파이썬" ) # %의 자리에 str을 넣겠다
print("Apple 은 %c로 시작해요." % "A") # %의 자리에 c(character)를 넣겠다

print("나는 %s살입니다." % 20) # %의 자리에 str 타입으로 20을 넣겠다
print("나는 $s색과 %s색을 좋아해요." % ("파란", "빨간"))